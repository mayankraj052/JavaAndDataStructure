package Operator_in_java.JavaArithmeticOperator;

public class Example1 {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println(a & b);
    }
}