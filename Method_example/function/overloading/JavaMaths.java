package Method_example.function.overloading;

public class JavaMaths {
    public static void main(String[] args) {
        // int arr[] = { 5, 3, 8, 1, 8 };
        System.out.println(Math.min(8, 9));
        System.out.println(Math.max(8, 9));
        System.out.println(Math.sqrt(625)); // result in float
        System.out.println(Math.pow(5, 4));// result in float.
        System.out.println(Math.abs(-700.99)); // provide posotive always
    }
}
